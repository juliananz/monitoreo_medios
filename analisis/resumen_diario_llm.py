# CLI
# =============================================================================

if __name__ == "__main__":
    import sys

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
    )

    if len(sys.argv) > 1:
        try:
            target_date = date.fromisoformat(sys.argv[1])
        except ValueError:
            print(f"Formato de fecha inválido: {sys.argv[1]}. Usa YYYY-MM-DD")
            sys.exit(1)
    else:
        target_date = date.today()

    result = generar_resumen_diario_llm(target_date)
    if result:
        print(f"\nResumen generado: {result}")
        print("\nContenido:")
        print("-" * 50)
        print(result.read_text(encoding="utf-8"))
    else:
        print("No se pudo generar el resumen. Verifica que GEMINI_API_KEY esté configurada.")
        sys.exit(1)
